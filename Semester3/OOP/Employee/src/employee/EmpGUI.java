    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // TODO add your handling code here:
        //if the basicRB then add those fields to a tempE obj

        if (employeeRB.isSelected()) {
            //need a temp Employee obj
            Employee tempE = new Employee();
            //get the date from the textfields on the JFrame Form
            String name = nameTF.getText();
            tempE.setName(name);
            tempE.setId(idTF.getText());
            //add the temp Employee obj to the arraylist
            elist.add(tempE);
            //reply to user
            displayTA.setText("employee added: " + tempE.getName() + "\n size of elist: " + elist.size());
            //clear down text fields
            nameTF.setText("");
            idTF.setText("");
        } else if (cleanerRB.isSelected()) {//else the floorStaffRB add those fields to tempFS obj
            Cleaner tempFS = new Cleaner();
            tempFS.setName(nameTF.getText());
            tempFS.setId(idTF.getText());
            tempFS.setHourly(Double.parseDouble(rateTF.getText()));
            tempFS.setHours(Integer.parseInt(hoursTF.getText()));
            elist.add(tempFS); //add to elist
            displayTA.setText("employee added: " + tempFS.toString() + "\n size of elist: " + elist.size());
            //clear fields
            nameTF.setText("");
            idTF.setText("");
            hoursTF.setText("");
            rateTF.setText("");
        }

    }//GEN-LAST:event_addBTNActionPerformed

