    private void loadBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBTNActionPerformed
        //load from file

        File f; //declarations
        FileInputStream fs;
        ObjectInputStream os;

        try {
            f = new File("log.dat"); //creates
            fs = new FileInputStream(f);
            os = new ObjectInputStream(fs);
            elist = (ArrayList<Employee>) os.readObject();
            //displayTA.append("read from file, size: " +elist.size()); //test line      
            os.close();
        } catch (IOException e) {
            System.out.println("Error reading from file " + e);
        } catch (ClassNotFoundException c) {
            System.out.println("Class not found error " + c);
        }

    }//GEN-LAST:event_loadBTNActionPerformed

    private void saveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTNActionPerformed
        File f; //file
        FileOutputStream fs;
        ObjectOutputStream os;
        //try catch
        try {
            f = new File("log.dat");
            fs = new FileOutputStream(f);
            os = new ObjectOutputStream(fs);

            os.writeObject(elist);
            os.close();
            displayTA.setText("Employee list written to file");
            //close
        } catch (IOException e) {
            System.out.println("Error writing to file: " + e);
        }
    }//GEN-LAST:event_saveBTNActionPerformed

    private void displayBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBTNActionPerformed
//displayBTN code

        for (int i = 0; i < elist.size(); i++) {
            Employee e = elist.get(i);
            displayTA.append(e.getName() + "\n");
        }
    }//GEN-LAST:event_displayBTNActionPerformed

